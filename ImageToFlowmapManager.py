    def get_editor(self, Val):
        editor = QLineEdit()
        if isinstance(Val, int):
            editor.setText(str(Val))
            editor.setValidator(QtGui.QIntValidator())
        elif isinstance(Val, float):
            class FlexibleDoubleValidator(QtGui.QDoubleValidator):
                def __init__(self, bottom=0.0, top=100.0, decimals=3, parent=None):
                    super().__init__(bottom, top, decimals, parent)
                    self.setNotation(QtGui.QDoubleValidator.StandardNotation)
                    self.setLocale(QLocale(QLocale.English))  # erlaubt '.'
                def validate(self, input_str, pos):
                    # Ersetze Komma durch Punkt, bevor Standardprüfung greift
                    input_str = input_str.replace(',', '.')
                    return super().validate(input_str, pos)
                def fixup(self, input_str):
                    return input_str.replace(',', '.')
            editor.setText(str(Val))
            validator = FlexibleDoubleValidator(decimals=4)
            editor.setValidator(validator)
        else:  # string oder sonstiges
            editor.setText(str(Val))
        return editor
    
