    def check_thread_progress(self, ProcessType : processtypes, queue, timer):
        if self.checkingprogress: return
        self.checkingprogress = True
        if queue.empty():
            if ProcessType == "Clustering":
                self.joke_update_progressbar(ProcessType)
            self.checkingprogress = False
            return
        
        while not queue.empty():            
            val = queue.get()
            #print(f"PT: ({ProcessType}) got val ({val}))")
            if type(val) == int or type(val) == float:
                if val == 100:
                    self.stop_timer(timer)
                else:
                    self.update_progressbar(ProcessType, val)
            elif not isinstance(val, Exception): #we give back keyword dictionary
                try:
                    self.process_queuetrigger(ProcessType, val, timer)
                except Exception as e:
                    print(f"Error in process_queuetrigger: ({e})")
            else:
                print(f"Error: ({val}), type ({type(val)})")
                #self.threadupdatetimer[ProcessType] == None
        self.checkingprogress = False

