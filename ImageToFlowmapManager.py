    def update_clustermethod(self):
        #checks if there is any cluster image for the selected image using the selected method
        #if there is an cluster image, we take it until user wants to reload it because clustering takes time.. 
        CurFlowMapGenFiles : FlowMapGenFiles = self.get_current_flowmapgenfile()
        self.OpenGL_MainImage.doupdates = False
        if CurFlowMapGenFiles and os.path.exists(CurFlowMapGenFiles.ClusterFile.fullname):
            if self.hadcluster:                
                self.OpenGL_MainImage.remove_layer(ImageLayer, "Cluster_", True)
                for cluster in self.clusterobj.cluster:
                    newimg = np.full((cluster.clustermask.shape[0], cluster.clustermask.shape[1], 4), (-1,-1,-1,-1))
                    newimg[cluster.clustermask] = cluster.clusterid
                    self.OpenGL_MainImage.add_layer(ImageLayer, newimg, cluster.layername, 1, 0.5, False, 0, 1, (1,1,1))
            else:
                self.clusterobj : clusters = clusters([], "Cluster", CurFlowMapGenFiles.Clusters.fullname)
                self.clusterobj.rename_clusters_members_numeration()
                labels = self.OpenGL_MainImage.load_image_cv2(CurFlowMapGenFiles.ClusterFile.fullname)
                labels = labels.astype(np.int64)
                alpha = self.OpenGL_MainImage.load_image_cv2(CurFlowMapGenFiles.BaseFile.fullname)
                alpha = alpha[:,:,3].astype(bool)
                labels[~alpha, :] = -1
                #labels.swapaxes(0,1)
                uniques = np.unique(labels.reshape(-1,labels.shape[-1]), axis=0)
                self.OpenGL_MainImage.doupdates = False
                self.OpenGL_MainImage.remove_layer(ImageLayer, "Cluster_", True)
                for unique in uniques:
                    if unique[0] == -1: continue
                    mask = np.all(labels==unique, axis=-1)
                    newimg = np.zeros(labels.shape)
                    #if unique[0] == 0: unique = [255, 255, 255, 255]
                    newimg[mask] = unique
                    newimg[~mask] = [-1,-1,-1,0]
                    newcluster = self.clusterobj.add_cluster(mask, [], clusterid = unique, name = self.clusterobj.layername)
                    self.clusterobj.rename_clusters_members_numeration()
                    self.OpenGL_MainImage.add_layer(ImageLayer, newimg, newcluster.layername, 1, 0.5, False, 0, 1, (1,1,1))
                    
                self.clusterobj.save()
            val = 100            
        else:
            self.OpenGL_MainImage.doupdates = False
            self.OpenGL_MainImage.remove_layer(ImageLayer, "Cluster_", True)
            gclusters = [c for c in self.clusterobj.cluster]
            for cluster in gclusters:
                self.clusterobj.cluster.remove(cluster)
            val = 0
        self.OpenGL_MainImage.doupdates = True
        #if val != self.progressBar_ClusterMethod.value():
        self.update_clustertable()  #
        self.update_progressbar("Clustering",val)
        self.SCRLB_ClusterAlpha.setValue(val)
        #self.update_layertable()

