    def add_pointimage_to_layer(self, junction : junction, h, w, name, updateexisting = True):
        junction.img = np.zeros((h,w, 4), dtype=np.uint8)
        #print(f"add_pointimage_to_layer: junctiontype: ({type(junction)})")
