    def get_relative_progress(self, minimum : float | int, optimum : float | int, maximum : float | int, current : float | int):
        if current > optimum:
            if maximum != optimum: 
                ratio = 1 - (maximum - current) / (maximum - optimum)
            else:
                ratio = 1 - (maximum - current) / (maximum + 0.0001 - optimum)
        else:
            if optimum != minimum:
                ratio = (optimum - current) / (optimum - minimum)
            else:
                ratio = (optimum - current) / (optimum + 0.0001 - minimum)
        return ratio
    
