class ManagerProcess:   #is instanced as subprocess
    def __init__(self, queuein : Queue, refreshrate = 10000):
        self.in_q = queuein   
        self.subprocesses = {}
        self.refreshrate = refreshrate
        self.terminatequeue = Queue()   
        self.lastupdate = time.time()
        self.notdead = True

    def setrefreshrate(self, ms : int):
        self.refreshrate = ms

    def run(self):
        while self.notdead:
            newtime = time.time()
            if not self.in_q.empty():
                a = self.in_q.get()
                if type(a) == dict:
                    if len(a) == 1:
                        k, i = list(a.items())[0]
                        if k == "set_refreshrate":
                            self.setrefreshrate(i)
                            continue
                        elif k == "stop":
                            self.stop()
                        elif k == "stop_workerprocesses":
                            self.stop_workerprocesses(i)
                elif newtime - self.lastupdate >= self.refreshrate / 1000:                    
                    fntype, fn, args = a
                    if fntype in self.subprocesses: # we kill old processes
                        self.stop_workerprocess(fntype)
                    self.subprocesses[fntype] = BetterProcess(Process(
                        target=fn,
                        args=tuple(*[args + [self.terminatequeue, fntype]])
                    ), TerminateQueue=self.terminatequeue)
                    self.subprocesses[fntype].Process.start()
                    self.lastupdate = newtime
                while not self.terminatequeue.empty():
                    fntype = self.terminatequeue.get()
                    self.stop_workerprocess(fntype)

    def get_process(self, fntype):
        if fntype in self.subprocesses:
            return self.subprocesses[fntype]
        return None
    
    def start(self):
        self.poll()

    def stop(self):
        self.stop_workerprocesses()
        self.notdead = False
        sys.exit()

    def stop_workerprocesses(self, WorkerProcessName : str = "", StopMultiple = True):
        tokill = []
        for k in self.subprocesses.keys():
            if WorkerProcessName in k:
                tokill.append(k)
                if not StopMultiple:
                    break
        for k in tokill:
            self.stop_workerprocess(k)
            

    def stop_workerprocess(self, ProcessType : processtypes):
        if ProcessType in self.subprocesses and hasattr(self.subprocesses[ProcessType], "Process"): #if None will skip
            if self.subprocesses[ProcessType].Process.is_alive():
                self.subprocesses[ProcessType].TerminateQueue.put(True)
                self.subprocesses[ProcessType].Process.join(timeout=1)
                if self.subprocesses[ProcessType].Process.is_alive():
                    self.subprocesses[ProcessType].Process.terminate()
            
            self.subprocesses[ProcessType].Process = None
            
            self.subprocesses[ProcessType].UpdateQueue = None
            self.subprocesses[ProcessType].TerminateQueue = None
            
            if self.subprocesses[ProcessType].QTimerUpdate: 
                self.stop_timer(self.subprocesses[ProcessType].QTimerUpdate)
                self.subprocesses[ProcessType].QTimerUpdate = None
            if self.subprocesses[ProcessType].QTimerTerminate:
                self.stop_timer(self.subprocesses[ProcessType].QTimerTerminate)
                self.subprocesses[ProcessType].QTimerTerminate = None
            self.subprocesses[ProcessType] = None
            del self.subprocesses[ProcessType]

class BetterProcess():
    def __init__(self, TheProcess : Optional[Process], QTimerUpdate : Optional[QTimer] = None, QTimerTerminate : Optional[QTimer] = None, UpdateQueue : Optional[Queue] = None, TerminateQueue : Optional[Queue] = None):
        super().__init__()
        self.Process            : Optional[Process] = TheProcess
        self.QTimerUpdate       : Optional[QTimer]  = QTimerUpdate
        self.QTimerTerminate    : Optional[QTimer]  = QTimerTerminate
        self.UpdateQueue        : Optional[Queue]   = UpdateQueue
        self.TerminateQueue     : Optional[Queue]   = TerminateQueue

    def start(self):
        if self.Process:
            self.Process.start()
            return True
        return False


