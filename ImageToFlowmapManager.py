    def load_table(self, data : dict):
        table : QTableWidget = self.findChild(QTableWidget, data["object_name"])
        realdata = data["data"]
        SaveIndexes = "SaveIndex" in self.savetables[table]
        SaveContent = "SaveContent" in self.savetables[table]
        lastselectedindexes = data["lastselectedindexes"] if SaveIndexes and "lastselectedindexes" in data.keys() else None

        if SaveContent:
            table.setRowCount(len(realdata))
            table.setColumnCount(len(realdata[0]) if realdata else 0)
            for row, row_data in enumerate(realdata):
                for col, cell in enumerate(row_data):
                    if cell is not None:
                        item = QTableWidgetItem(cell["text"])
                        item.setData(256, cell["user_data"])
                        table.setItem(row, col, item)
        if lastselectedindexes and lastselectedindexes != (-1,-1):
            table.clearSelection()
            for row, col in lastselectedindexes:
                idx = table.model().index(row, col)
                table.blockSignals(True)
                #table.selectionModel().select(
                #    idx,
                #    QItemSelectionModel.Select | QItemSelectionModel.Rows  # oder .Columns / .NoUpdate
                #)
                table.setCurrentIndex(idx)
